package com.mhd.elemantary.common.vo;

/**
 * schedule data class
 * Created by MH.D on 2021-06-23.
 */
public class ScheduleData {
	private String time;
	private String mon;
	private String tue;
	private String wed;
	private String thu;
	private String fri;
	private String sat;
	private String sun;

	public ScheduleData() {
	}

	public String getTime() {
		return time;
	}
	public void setTime(String title) {
		this.time = title;
	}
	public String getMon() {
		return mon;
	}
	public void setMon(String title) {
		this.mon = title;
	}
	public String getTue() {
		return tue;
	}
	public void setTue(String title) {
		this.tue = title;
	}
	public String getWed() {
		return wed;
	}
	public void setWed(String title) {
		this.wed = title;
	}
	public String getThu() {
		return thu;
	}
	public void setThu(String title) {
		this.thu = title;
	}
	public String getFri() {
		return fri;
	}
	public void setFri(String title) {
		this.fri = title;
	}
	public String getSat() {
		return sat;
	}
	public void setSat(String title) {
		this.sat = title;
	}
	public String getSun() {
		return sun;
	}
	public void setSun(String title) {
		this.sun = title;
	}
}
