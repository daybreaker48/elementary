package com.mhd.stard.common.vo;

/**
 * Kids data class
 * Created by MH.D on 2021-10-25.
 */
public class KidsData {
	private String kidsname;
	private String kidsage;
	private String kidsidx;

	public String getKidsname() {
		return kidsname;
	}
	public void setKidsname(String kidsname) {
		this.kidsname = kidsname;
	}
	public String getKidsage() {
		return kidsage;
	}
	public void setKidsage(String kidsage) {
		this.kidsage = kidsage;
	}
	public String getKidsidx() {
		return kidsidx;
	}
	public void setKidsidx(String kidsidx) {
		this.kidsidx = kidsidx;
	}
}
